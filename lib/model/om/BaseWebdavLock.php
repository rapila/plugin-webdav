	public function hydrate($row, $startcol = 0, $rehydrate = false)
	{
		try {

			$this->token = ($row[$startcol + 0] !== null) ? (string) $row[$startcol + 0] : null;
			$this->path = ($row[$startcol + 1] !== null) ? (string) $row[$startcol + 1] : null;
			$this->owner = ($row[$startcol + 2] !== null) ? (int) $row[$startcol + 2] : null;
			$this->is_recursive = ($row[$startcol + 3] !== null) ? (boolean) $row[$startcol + 3] : null;
			$this->is_exclusive = ($row[$startcol + 4] !== null) ? (boolean) $row[$startcol + 4] : null;
			$this->created_at = ($row[$startcol + 5] !== null) ? (string) $row[$startcol + 5] : null;
			$this->updated_at = ($row[$startcol + 6] !== null) ? (string) $row[$startcol + 6] : null;
			$this->expires_at = ($row[$startcol + 7] !== null) ? (string) $row[$startcol + 7] : null;
			$this->resetModified();

			$this->setNew(false);

			if ($rehydrate) {
				$this->ensureConsistency();
			}

			return $startcol + 8; // 8 = WebdavLockPeer::NUM_COLUMNS - WebdavLockPeer::NUM_LAZY_LOAD_COLUMNS).

		} catch (Exception $e) {
