	/**
	 * Build a Criteria object containing the values of all modified columns in this object.
	 *
	 * @return     Criteria The Criteria object containing all modified values.
	 */
	public function buildCriteria()
	{
		$criteria = new Criteria(WebdavLockPeer::DATABASE_NAME);

		if ($this->isColumnModified(WebdavLockPeer::TOKEN)) $criteria->add(WebdavLockPeer::TOKEN, $this->token);
		if ($this->isColumnModified(WebdavLockPeer::PATH)) $criteria->add(WebdavLockPeer::PATH, $this->path);
		if ($this->isColumnModified(WebdavLockPeer::OWNER)) $criteria->add(WebdavLockPeer::OWNER, $this->owner);
		if ($this->isColumnModified(WebdavLockPeer::IS_RECURSIVE)) $criteria->add(WebdavLockPeer::IS_RECURSIVE, $this->is_recursive);
		if ($this->isColumnModified(WebdavLockPeer::IS_EXCLUSIVE)) $criteria->add(WebdavLockPeer::IS_EXCLUSIVE, $this->is_exclusive);
		if ($this->isColumnModified(WebdavLockPeer::CREATED_AT)) $criteria->add(WebdavLockPeer::CREATED_AT, $this->created_at);
		if ($this->isColumnModified(WebdavLockPeer::UPDATED_AT)) $criteria->add(WebdavLockPeer::UPDATED_AT, $this->updated_at);
		if ($this->isColumnModified(WebdavLockPeer::EXPIRES_AT)) $criteria->add(WebdavLockPeer::EXPIRES_AT, $this->expires_at);

		return $criteria;
	}

